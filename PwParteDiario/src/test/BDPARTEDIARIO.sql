	  create or replace PROCEDURE GENERARPARTEDIARIO (P_LISTA OUT  SYS_REFCURSOR,P_USUARIO IN VARCHAR2)
AS 
dia NUMBER:=0;
IDMODALIDADgrupo24 NUMBER;
COUNTMODALIDADGRUPO24 NUMBER;
FLAGSF NUMBER;
BEGIN
--id modalidad alfa 1
--id modalidad bravo 2
-- YA SE HA GENERADO EN EL DIA
SELECT COUNT(NVL(NSF.IDMODALIDAD,0)) INTO COUNTMODALIDADGRUPO24  FROM NUMERADOR_SERVICIO_FRANCO NSF  WHERE TRUNC(NSF.FECHAREG)=TRUNC(SYSDATE);
IF(COUNTMODALIDADGRUPO24>0) THEN 
SELECT NVL(NSF.IDMODALIDAD,0) INTO IDMODALIDADgrupo24  FROM NUMERADOR_SERVICIO_FRANCO NSF  WHERE TRUNC(NSF.FECHAREG)=TRUNC(SYSDATE);
ELSE
IDMODALIDADgrupo24:=0;
END IF;
DBMS_OUTPUT.PUT_LINE(  'IDMODALIDADgrupo24 ' || IDMODALIDADgrupo24 );
IF(IDMODALIDADgrupo24=0)
THEN
FLAGSF:=0;
select decode(mod(trunc(to_date(sysdate,'DD/MM/YYYY hh24:mi:ss'))-trunc(to_date(NSF.FECHAREG,'DD/MM/YYYY hh24:mi:ss')),2),0,2,1,1) as diferencia_en_dias INTO IDMODALIDADgrupo24 
from NUMERADOR_SERVICIO_FRANCO NSF WHERE NSF.IDNUMSERFRA=1 ORDER BY 1 ASC;
ELSE
FLAGSF:=1;
END IF;

DBMS_OUTPUT.PUT_LINE(  'FLAGSF ' || FLAGSF );
SELECT DECODE(RTRIM(LTRIM(TO_CHAR(SYSDATE, 'DAY', 'NLS_DATE_LANGUAGE=SPANISH'))),
'LUNES', 1, 'MARTES', 2, 'MIÉRCOLES', 3, 'JUEVES', 4,
'VIERNES', 5, 'SÁBADO', 6, 'DOMINGO',7) AS day_week into dia
FROM dual;  
DBMS_OUTPUT.PUT_LINE(  'dia ' || dia );
IF(dia<7)
THEN
--DIA DE SEMANA L-S
IF(IDMODALIDADgrupo24=1)
--GRUPO 1 ALFA SERVICIO---  2 BRAVO FRANCO
THEN
IF(FLAGSF=0) THEN
INSERT INTO NUMERADOR_SERVICIO_FRANCO (  IDNUMSERFRA,  IDMODALIDAD, EQUIPO, ESTADO, FECHAREG, USUMOD, USUREG, FECHAMOD ) VALUES (SEC_NUMSERFRA.NEXTVAL,IDMODALIDADgrupo24,'ALFA 24X24', 1,SYSDATE,NULL,P_USUARIO,NULL);
END IF;
 open P_LISTA for
SELECT 
  AR.DESCRIPCION as AREA,
  GRA.DESGRAD as GRADO,
  EFE.APEPAT,
  EFE.APEMAT,
  EFE.NOMBRES,
  CAR.DESCRIPCION AS CARGO,
  MODA.HORARIO,
  MODA.DESCRIPCION AS MODALIDAD,
  JER.DESCRIPCION AS JERARQUIA,
  MAXN.IDSITUACION,
  NVL(SITU.DESCRIPCION,'DISPONIBLE') AS SITUACION,
 CONCAT(CONCAT(TO_CHAR(EFS.FECHAINI, 'DD/MM/YYYY'),' - '),NVL(TO_CHAR(EFS.FECHAFIN, 'DD/MM/YYYY'),' INDETERMINADO')) AS RANGO, 
  EFS.OBSERVACIONES,
CASE WHEN (MODA.IDMODALIDAD!=2) THEN 'SERVICIO'
ELSE 'FRANCO' END AS SERVICIO
FROM EFECTIVO EFE
INNER JOIN GRADO GRA ON GRA.TGRAD=EFE.GRADO
INNER JOIN AREA AR ON AR.IDAREA=EFE.IDAREA
INNER JOIN CARGO CAR ON CAR.IDCARGO=EFE.IDCARGO
INNER JOIN MODALIDAD MODA ON MODA.IDMODALIDAD=EFE.IDMODALIDAD
INNER JOIN JERARQUIA JER ON JER.IDJERARQUIA= EFE.IDJERARQUIA
LEFT JOIN (SELECT MAX(IDEFESIT) AS IDEFESIT , IDEFECTIVO, IDSITUACION FROM EFECTIVO_SITUACION  WHERE  ESTADO=1 GROUP BY IDEFECTIVO,IDSITUACION) MAXN ON MAXN.IDEFECTIVO=EFE.IDEFECTIVO
LEFT JOIN EFECTIVO_SITUACION EFS ON EFS.IDEFESIT=MAXN.IDEFESIT
LEFT JOIN SITUACION SITU ON SITU.IDSITUACION=EFS.IDSITUACION
WHERE EFE.ESTADO=1   ORDER BY AR.IDAREA ASC,GRA.TGRAD  DESC;
ELSE IF (IDMODALIDADgrupo24=2)
--GRUPO 2 BRAVO SERVICIO---  1 ALFRA FRANCO
